function toggleLang(){
  var c=localStorage.getItem('fz_lang')||'es';
  localStorage.setItem('fz_lang',c==='es'?'en':'es');
  _applyLang();
}
function _applyLang(){
  var l=localStorage.getItem('fz_lang')||'es',d=_D[l]||_D.es;
  document.querySelectorAll('[data-i18n]').forEach(function(el){
    var v=d[el.getAttribute('data-i18n')];
    if(v===undefined)return;
    if(el.tagName==='INPUT'||el.tagName==='TEXTAREA')el.placeholder=v;
    else el.textContent=v;
  });
  document.documentElement.lang=l;
  var f=document.getElementById('langFlag'),lb=document.getElementById('langLabel');
  if(l==='es'){if(f)f.textContent='🇬🇧';if(lb)lb.textContent='EN';}
  else{if(f)f.textContent='🇪🇸';if(lb)lb.textContent='ES';}
}
(function(){
  var btn=document.getElementById('langBtn')||document.getElementById('langToggleBtn');
  if(btn)btn.onclick=toggleLang;
  if(document.readyState==='loading'){document.addEventListener('DOMContentLoaded',_applyLang);}
  else{_applyLang();}
})();
